package pkg.staff;

public class StaffVO {
    private String staffIdx, staffId, staffNm, staffEnNm, regDate;

    public String getRegDate() {
        return regDate;
    }
    public void setRegDate(String regDate) {
        this.regDate = regDate;
    }
    public String getStaffEnNm() {
        return staffEnNm;
    }
    public void setStaffEnNm(String staffEnNm) {
        this.staffEnNm = staffEnNm;
    }
    public String getStaffId() {
        return staffId;
    }
    public void setStaffId(String staffId) {
        this.staffId = staffId;
    }
    public String getStaffIdx() {
        return staffIdx;
    }
    public void setStaffIdx(String staffIdx) {
        this.staffIdx = staffIdx;
    }
    public String getStaffNm() {
        return staffNm;
    }
    public void setStaffNm(String staffNm) {
        this.staffNm = staffNm;
    }
}
